//画斜线直线
void TFTLCD_Draw_Line(u16 x1,u16 y1,u16 x2,u16 y2,u16 color)
{
	u16 i=0;
	int delat_x=0,delat_y=0,xerr=0,yerr=0;
	int urow=x1,ucol=y1;
	int distance=0;
	int incx=0,incy=0;
	delat_x=x2-x1;
	delat_y=y2-y1;
	if(delat_x>0)
		incx=1;
	else if(delat_x==0)
		incx=0;
	else
		incx=-1;
	
	if(delat_y>0)
		incy=1;
	else if(delat_y==0)
		incy=0;
	else
		incy=-1;
	if(delat_x>delat_y)
		distance=delat_x;
	else
		distance=delat_y;
	for(i=0;i<=distance+1;i++)
	{
		TFTLCD_Dot(urow,ucol,color);
		xerr+=delat_x;
		yerr+=delat_y;
		if(xerr>distance)
		{
			xerr-=distance;
			urow+=incx;
		}
		if(yerr>distance)
		{
			yerr-=distance;
			ucol+=incy;
		}
	}
	
}

//画园
void LCD_Draw_Circle(u16 x0,u16 y0,u8 r)
{
	int a,b;
	int di;
	a=0;b=r;	  
	di=3-(r<<1);             //判断下个点位置的标志
	while(a<=b)
	{
		TFTLCD_Dot(x0+a,y0-b,BLACK);             //5
 		TFTLCD_Dot(x0+b,y0-a,BLACK);             //0           
		TFTLCD_Dot(x0+b,y0+a,BLACK);             //4               
		TFTLCD_Dot(x0+a,y0+b,BLACK);             //6 
		TFTLCD_Dot(x0-a,y0+b,BLACK);             //1       
 		TFTLCD_Dot(x0-b,y0+a,BLACK);             
		TFTLCD_Dot(x0-a,y0-b,BLACK);             //2             
  		TFTLCD_Dot(x0-b,y0-a,BLACK);             //7     	         
		a++;
		//使用Bresenham算法画圆     
		if(di<0)di +=4*a+6;	  
		else
		{
			di+=10+4*(a-b);   
			b--;
		}
		delay_ms(20);
	}
} 

void TFTLCD_Show_Char(u16 x,u16 y,u8 ch,u8 size,u8 mode)
{
	u8 tmp=0;
	u8 i=0;
	u8 j=0;
	u16 y0=y;
	u8 csize=(size/8+((size%8)?1:0))*(size/2);
	ch=ch-' ';
	for(i=0;i<csize;i++)
	{
		tmp=ascii_1608[ch][i];
		for(j=0;j<8;j++)
		{
			if(tmp&0x80)
			{
				TFTLCD_Dot(x,y,BLACK);
			}
			else if(mode==0)
			{
				TFTLCD_Dot(x,y,WHITE);
			}
			tmp<<=1;
			y++;
			if(y>=480)
			{
				return;
			}
			if((y-y0)==size)
			{
				y=y0;
				x++;
				if(x>=320)
				{
					return;
				}
				break;
			}
		}
	}
}

void TFTLCD_Show_String(u16 x,u16 y,u8* str,u8 size)
{
	u16 x0=x;
	while(*str!='\0')
	{
		TFTLCD_Show_Char(x,y,*str,size,0);
		str++;
		x+=size/2;
		if(x>=320)
		{
			x=x0;
			y+=size;
		}
		if(y>=480)
		{
			break;
		}
	}
}



void TFTLCD_Show_HZ(u16 x,u16 y,u8* cn)
{
	u16 row=0;
	u16 byte=0;
	u16 x0=x;
	u16 y0=y;
	u8 bit=0;
	u8 data=0;
	u8 wordnum=0;
	while(*cn!='\0')
	{
		if((hz[wordnum].hz[0]==*cn)&&(hz[wordnum].hz[1]==*(cn+1)))
		{
			for(row=0;row<32;row++)
			{
				for(byte=0;byte<4;byte++)
				{
					data=hz[wordnum].data[row*4+byte];
					for(bit=0;bit<8;bit++)
					{
						if(data&(0x80>>bit))
						{
							TFTLCD_Dot(x,y,BLACK);
						}
						else
						{
							TFTLCD_Dot(x,y,WHITE);
						}
						x++;
						if((x-x0)==32)
						{
							x=x0;
							y++;
							if((y-y0)==32)
							{
								y=y0;
							}
						}
					}
				}
			}
		}
		x+=32;
		x0=x;
		wordnum++;
		cn+=2;
	}
}

u16 TFTLCD_Pow(u16 x,u16 y)
{
	u16 tmp=1;
	while(y--)
	{
		tmp*=x;
	}
	return tmp;
}

