	{
		return;
	}
	TFTLCD_Set_Window(xsta,ysta,xend,yend);
	u16 xtmp=xend-xsta+1;
	u16 ytmp=yend-ysta+1;
	while(xtmp--)
	{
		while(ytmp--)
		{
			LCD_Write_color(color);
		}
	}
