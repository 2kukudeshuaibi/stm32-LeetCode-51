//0:竖屏  1:横屏
void TFTLCD_Set_dir(u8 dir)
{
	if(dir)
	{
		LCD_WriteCmd(0x36);
		LCD_WriteData(0x28);
		LCD_WriteCmd(0xb6);
		LCD_WriteData(0x00);
		LCD_WriteData(0x02);
		LCD_WriteData(0x3b);
	}
	else
	{
		LCD_WriteCmd(0x36);
		LCD_WriteData(0x08);
	}
}

void FULL_COloc(u16 color)
{
	int tt=320*480;
	LCD_WriteCmd(0x2a);
	LCD_WriteData(0x00);
	LCD_WriteData(0x00);
	LCD_WriteData(0x01);
	LCD_WriteData(0x3f);
	
	LCD_WriteCmd(0x2b);
	LCD_WriteData(0x00);
	LCD_WriteData(0x00);
	LCD_WriteData(0x01);
	LCD_WriteData(0xdf);
	
	LCD_WriteCmd(0x2c);
	
	while(tt--)
	{
		LCD_WriteData((color>>8)&0xff);
		LCD_WriteData(color&0xff);
	}
	
}
//建立窗口
